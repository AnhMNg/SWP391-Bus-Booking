package sample.dto;

/**
 *
 * @author baolo
 */
public class Place {
    private long placeId;
    private String name;

    public Place() {
    }

    public Place(long placeId, String name) {
        this.placeId = placeId;
        this.name = name;
    }

    public long getPlaceId() {
        return placeId;
    }

    public void setPlaceId(long placeId) {
        this.placeId = placeId;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
}
